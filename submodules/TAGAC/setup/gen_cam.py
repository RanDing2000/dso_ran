import numpy as np
import pyperclip

# 定义外参矩阵
extrinsic_matrix = np.array([
    [1, 6.123233995736767e-17, 3.0814879110195774e-33, -0.15000000000000002],
    [3.0616169978683836e-17, -0.5000000000000001, -0.8660254037844387, 0.1616025403784439],
    [-5.3028761936245346e-17, 0.8660254037844387, -0.5000000000000001, 0.5200961894323343],
    [0, 0, 0, 1]
])

# 将矩阵转换为字符串格式
matrix_str = '\n'.join(' '.join(map(str, row)) for row in extrinsic_matrix)

# 复制到剪贴板
pyperclip.copy(matrix_str)
print("外参矩阵已复制到剪贴板")
